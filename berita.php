                <p>
                Pucangan, [8 mei 2025] — Dalam upaya mencegah penyalahgunaan narkotika di kalangan pemuda, Karang Taruna Desa Pucangan mengadakan kegiatan sosialisasi bertema "Generasi Muda Bersih, Masa Depan Cerah" pada [hari, tanggal] di Balai Desa Pucangan.

Kegiatan ini dihadiri oleh lebih dari 100 peserta, yang terdiri dari remaja, pemuda, dan orang tua. Hadir sebagai narasumber adalah [agus setiawan ], perwakilan dari Badan Narkotika Nasional (BNN) Kabupaten [TUBAN], serta [WAHYU SETIAWAN], seorang praktisi kesehatan yang telah lama berkecimpung dalam program rehabilitasi penyalahgunaan narkotika.

Dalam paparannya,  menjelaskan dampak buruk narkotika, baik secara fisik, psikologis, maupun sosial. Ia menekankan bahwa penyalahgunaan narkoba tidak hanya merusak masa depan individu, tetapi juga dapat menghancurkan kehidupan keluarga dan masyarakat.

"Kami ingin generasi muda di Desa Pucangan memahami betapa berbahayanya narkoba. Hindari bujuk rayu teman atau lingkungan yang mengajak pada hal-hal negatif," ujar Narasumber.

Selain itu,  Pemateri memberikan tips pencegahan, termasuk cara meningkatkan kepercayaan diri, memilih lingkungan pertemanan yang sehat, dan mengembangkan minat serta bakat. Ia juga mendorong peserta untuk tidak ragu melapor jika mengetahui ada kasus penyalahgunaan narkotika di lingkungan sekitar.

Ketua Karang Taruna Desa Pucangan, [SAMSUL ARIPIN], mengungkapkan bahwa kegiatan ini merupakan bentuk kepedulian Karang Taruna terhadap masa depan generasi muda di desa. "Kami ingin anak-anak muda di sini menjadi generasi yang sehat, bebas dari narkoba, dan mampu berkontribusi untuk desa," katanya.

Acara ini ditutup dengan sesi tanya jawab interaktif dan pembagian brosur edukasi. Peserta tampak antusias, salah satunya, [TONO], mengatakan, "Saya jadi lebih paham bahayanya narkoba dan tahu harus ke mana jika butuh bantuan atau informasi."

Karang Taruna berharap kegiatan ini dapat meningkatkan kesadaran masyarakat, khususnya generasi muda, tentang pentingnya menjauhi narkotika demi masa depan yang lebih baik.
